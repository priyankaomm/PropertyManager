    private void passActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_passActionPerformed
        // TODO add your handling code here:
        try {
            Connection con = MyConnection.createConnection();

            String username=user.getText();
            String Password=pass.getText();
            
            String sql = "select Id,username,password,profile_picture from user_registration where username=? and password=?  ";
            PreparedStatement psmt = con.prepareStatement(sql);
            psmt.setString(1, username);
            psmt.setString(2, Password);
            ResultSet rs = psmt.executeQuery();
            System.out.println("row:" + rs.getRow());
            rs.last();
            if (rs.getRow() > 0) {
                rs.beforeFirst();
                while (rs.next()) {
                   user.setText("");
                        pass.setText("");
                       
                         //JOptionPane.showMessageDialog(null, "Login Successfully!!!");
                         LoginPage log=new LoginPage();
                         log.setVisible(false);
                         dispose();   
                         //Stage stage=new Stage();
                         InputStream photo=rs.getBinaryStream("profile_picture");
                         createScene(rs.getInt("Id"),photo);
                         
                }
            } else {
                // invalid username or password
                 user.setText("");
                 pass.setText("");
                JOptionPane.showMessageDialog(null, "Invalide Username & Password!!!");
                user.requestFocus();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
                
        
    }//GEN-LAST:event_passActionPerformed

