private  void createScene(int id,InputStream photo)  {
    
        Platform.runLater(new Runnable() {
        @Override
        public void run() {
          //javaFX operations should go here
        try{
        FXMLLoader fxmlLoader = new FXMLLoader(getClass().getResource("FXMLDocument.fxml"));
                     
                     Parent root1 = (Parent) fxmlLoader.load();
                     Stage stage = new Stage();
                     FXMLDocumentController fc=new FXMLDocumentController();
                     Pane p=fc.loadMain(id);
                     ((Label)root1.lookup("#builder_id")).setText(""+id);
                     ((ScrollPane)root1.lookup("#scroll_pane")).setContent(p);
                     if(photo !=null)
                     {
                          Image image = new Image(photo);
                         ((ImageView)root1.lookup("#profile_photo")).setImage(image);
                     }
                     Scene scene = new Scene(root1);
                     stage.initStyle(StageStyle.UNDECORATED);
                     stage.setScene(scene);
                     stage.show();
               
    }catch(Exception e)
    {
        e.printStackTrace();
    }
      }
   });        
    }
    
